export function getLngLat(row: ShanghaiFootballVenueRow): { lng: number; lat: number } {
  if (row.lng != null && row.lat != null && !Number.isNaN(row.lng) && !Number.isNaN(row.lat)) {
    return { lng: row.lng, lat: row.lat }
  }
  return { lng: SHANGHAI_CENTER_LNG, lat: SHANGHAI_CENTER_LAT }
}

export function getDistrictCode(districtName: string | undefined): string | undefined {
  if (!districtName || !districtName.trim()) return undefined
  const key = districtName.trim().replace(/^上海/, '')
  return DISTRICT_MAP[key] || DISTRICT_MAP[districtName.trim()] || undefined
}

/** 从设施信息原文解析 hasLighting、hasParking 等 */
export function parseFacilities(facilities: string | undefined): {
  hasLighting?: boolean
  hasParking?: boolean
  hasFence?: boolean
  hasRestArea?: boolean
  hasShower?: boolean
  hasAirConditioning?: boolean
} {
  if (!facilities || !facilities.trim()) return {}
  const s = facilities
  return {
    hasLighting: /灯光|照明/i.test(s) || /有灯光/i.test(s),
    hasParking: /停车|停车场/i.test(s),
    hasFence: /围栏/i.test(s),
    hasRestArea: /休息区/i.test(s),
    hasShower: /淋浴/i.test(s),
    hasAirConditioning: /空调/i.test(s),
  }
}

